// Increment

let x= 20;
x= x+20;    // x += 20;
x= x+30;    // x += 30;

console.log("x ",x);

// Decrement

let y= 10;
y = y-5;    // y -= 5;
y = y-5;    // y -= 5;

console.log("y ",y);


let a = 10;
a = a+2;   // 1st
a += 10;   // 2nd is short hand
a++;       // incrementing by 1
++a;       // incrementing by 1

console.log("a ",a);


let b = 10;
b = b-2;   // 1st
b -= 10;   // 2nd is short hand
b--;       // decrementing by 1
--b;

console.log("b ",b);